def oriented_incidence(n_vertices: int, edges: List[Tuple[int, int]]) -> np.ndarray:
    D = np.zeros((n_vertices, len(edges)))
    for e, (a, b) in enumerate(edges):
        D[a, e] = -1.0
        D[b, e] = +1.0
    return D


