def stress_invariant(generators: List[np.ndarray]) -> float:
    total = 0.0
    for i in range(len(generators)):
        for j in range(i + 1, len(generators)):
            comm = generators[i] @ generators[j] - generators[j] @ generators[i]
            total += float(np.real(np.trace(comm.conj().T @ comm)))
    return total


