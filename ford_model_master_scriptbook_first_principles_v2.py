def solve_seam_weights(G1: int, G2: int, G3: int) -> Tuple[int, int, int, int]:
    I = np.array([
        [1, 1, 0],
        [1, 0, 1],
        [0, 1, 1],
    ], dtype=float)
    B_vec = np.array([G1, G2, G3], dtype=float)
    w = np.linalg.solve(2 * I, B_vec)
    rounded = tuple(int(round(x)) for x in w)
    assert np.allclose(w, rounded), f"Non-integer seam weights: {w}"
    return rounded[0], rounded[1], rounded[2], sum(rounded)


